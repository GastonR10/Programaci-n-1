class Locales {
  constructor(
    id,
    nombreL,
    usuarioL,
    password,
    tipoDeLocal,
    direccion,
    cupoMaximo,
    foto,
    habilitado
  ) {
    this.id = id;
    this.nombreL = nombreL;
    this.usuarioL = usuarioL;
    this.password = password;
    this.tipoDeLocal = tipoDeLocal;
    this.direccion = direccion;
    this.cupoMaximo = cupoMaximo;
    this.foto = foto;
    this.habilitado = habilitado;
  }
}
